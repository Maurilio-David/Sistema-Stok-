        public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDAO user = new UsuarioDAO();
        
        for(Usuario u: user.read()){
            
            modelo.addRow(new Object[]{
               u.getId(),
               u.getLogin(),
               u.getSenha()
            });
        
        
    }
    }
  public void readJTableForLogin(String login){
            
        DefaultTableModel modelo = (DefaultTableModel) jTUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDAO user = new UsuarioDAO();
        
        for(Usuario u: user.readForLogin(login)){
            
            modelo.addRow(new Object[]{
               u.getId(),
               u.getLogin(),
               u.getSenha()
            });
            
        }
     }
