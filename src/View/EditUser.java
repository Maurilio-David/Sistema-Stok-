    private void jTUsuarioMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTUsuarioMouseReleased

        if(jTUsuario.getSelectedRow() != -1){

            txtLogin.setText(jTUsuario.getValueAt(jTUsuario.getSelectedRow(), 1) .toString());
            txtSenha.setText(jTUsuario.getValueAt(jTUsuario.getSelectedRow(), 2) .toString());
            
        } else{
            JOptionPane.showMessageDialog(null, "Selecione um produto");

        }
    }//GEN-LAST:event_jTUsuarioMouseReleased

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(jTUsuario.getSelectedRow() != -1){
            Usuario u = new Usuario();
            UsuarioDAO dao = new UsuarioDAO();

            u.setId((int)jTUsuario.getValueAt(jTUsuario.getSelectedRow(), 0));
            dao.delete(u);

            readJTable();

        } else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if(jTUsuario.getSelectedRow() != -1){
            Usuario u = new Usuario();
            UsuarioDAO dao = new UsuarioDAO();

            u.setLogin(txtLogin.getText());
            u.setSenha((txtSenha.getText()));
            u.setId((int)jTUsuario.getValueAt(jTUsuario.getSelectedRow(), 0));
            dao.update(u);

            readJTable();

        }

    }//GEN-LAST:event_jButton3ActionPerformed

